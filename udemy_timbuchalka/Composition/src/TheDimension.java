public class TheDimension {
    private double length;
    private double breadth;
    private double height;
    public TheDimension(double length, double breadth, double height){
        this.length = length;
        this.breadth = breadth;
        this.height = height;
    }

    public double getBreadth() {
        return breadth;
    }

    public double getHeight() {
        return height;
    }

    public double getLength() {
        return length;
    }
}
